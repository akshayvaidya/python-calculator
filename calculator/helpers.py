def add(a,b) :
    if isinstance(a, (int,float)) and isinstance(b,(int,float)) :
        return a+b
    return

def sub(a,b) :
    if isinstance(a, (int,float)) and isinstance(b,(int,float)) :
        return a-b
    return    
    
def mul(a,b) :
    if isinstance(a, (int,float)) and isinstance(b,(int,float)) :
        return a*b
    return

def div(a,b) :
    if isinstance(a, (int,float)) and isinstance(b,(int,float)) and b != 0 :
        return a/b
    return

    